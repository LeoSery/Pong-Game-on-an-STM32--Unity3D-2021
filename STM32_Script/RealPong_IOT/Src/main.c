int main(void)
{
 
	uint8_t adcBuffer[2];
	uint8_t adcBuffer2[2];
 
  HAL_Init();
 
  SystemClock_Config();
 
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_I2S3_Init();
  MX_SPI1_Init();
  MX_USB_HOST_Init();
  MX_USART2_UART_Init();
  MX_ADC2_Init();
 
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *)adcBuffer, 2);
  HAL_ADC_Start_DMA(&hadc2, (uint32_t *)adcBuffer2, 2);
  ecris_txt("Salut les gars !\n");
  while (1)
  {
	  uint8_t v1 = adcBuffer[0];
	  uint8_t v2 = adcBuffer[1];
	  uint8_t v3 = adcBuffer2[0];
	  uint8_t v4 = adcBuffer2[1];
	  char mess[30];
	  sprintf(mess, "%X|%X|%X|%X \r\n", v1, v2, v3, v4);
	  ecris_txt(mess);
   
    MX_USB_HOST_Process();   
  }
 
}

